    class MyTimerTask extends TimerTask {

        FormEchoClient GuiInduk;
        private int timer;

        public MyTimerTask(FormEchoClient Gui) {
            this.GuiInduk = Gui;
            this.timer = 60;
        }

        public void run() {
            this.GuiInduk.labelJam.setText(Integer.toString(timer--));
            if (timer < 0) {
                this.timer = 60;
            }
        }
    }
